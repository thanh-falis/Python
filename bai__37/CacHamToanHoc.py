# Các hàm toán học

# import tất cả các hàm trong thư viện math
from math import *

x = 5
y = 7

# Mũ
print(pow(x, y))
print(x**y)

# Căn bậc 2
print('sqrt(25) = ', sqrt(25))

# Độ và Radians chuyển qua lại
do = 30
print("30 => ", radians(do))

rad = radians(do)
print(degrees(rad))

# Tính Log
print("log10(100)", log10(100))
print("log = ", log(2))


# Tính e mũ x
print("exp(2) = ", exp(2))

